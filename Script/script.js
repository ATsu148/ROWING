$(window).on("resize", function() {
    var w = $(window).width();
    if(w < 1153) {
      window.location = window.location;
    }
    if(w < 800) {
      window.location = window.location;
    }
    if(w < 600) {
      window.location = window.location;
    }
    if(w < 480) {
      window.location = window.location;
    }
  });